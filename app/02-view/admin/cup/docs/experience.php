    <h2>Managed setup — ikke self-service ennå</h2>
    <p>
        Store cuper settes opp som <strong>managed config</strong> i <code>bifrost-public-ui/config/cups/*.json</code>.
        Bifrost-teamet kuraterer profil, layout, innhold og sponsorer. Full redigering i admin kommer senere.
    </p>
    <ul>
        <li><strong>Self-service</strong> (fremtid): CupAdmin redigerer branding, meny og sponsorer i admin-UI.</li>
        <li><strong>Managed setup</strong> (nå): JSON-filer versjoneres i git og deployes med public-ui.</li>
    </ul>
    <p>
        Se <code>bifrost-shared/reference/cup-experience.md</code> for konsept, sponsorflater og pakkenivåer
        (Basis, Pluss, Partner).
    </p>
    <p class="muted">
        Denne siden er kun informativ — ingen lagring eller redigering av cup-config her.
    </p>
